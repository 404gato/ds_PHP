    <p class="card-text">
        <b>1 - Shingenki no Kyojin(Attack on Titan)</b>

        Motivo: Muita violência e sangue

Sinopse:

“A história gira em torno de Eren Yeager em um mundo onde a humanidade vive rodeada por muralhas para se proteger dos Titãs. Mas Eren não sabe que a origem destes seres, esta repleta de mistérios e revelações…”

Classificado como R (restrito), Shingeki no Kyojin é considerado impróprio para o consumo público na China (assim sendo banido) por sua extrema violência e derramamento de sangue.
    </p>

    <p class="card-text">
        2 - Corpse Party: Tortured Souls  (Bougyakusareta Tamashii no Jukyou)
<b>Motivo:</b> Muita violência

<b>Sinopse:</b>

A história original passa-se em Heavenly Host, uma escola primária que foi demolida após os assassinatos e desaparecimentos de diversos alunos e funcionários. Uma noite, um grupo de estudantes da Academia Kisaragi, que mais tarde fora construída no mesmo local onde Heavenly Host existia, reúnem-se para se despedir de uma colega que iria ser transferida no dia seguinte. Subitamente, um terremoto transporta-os para uma dimensão alternativa onde Heavenly Host ainda existe e na qual residem os espíritos das crianças assassinadas. E os estudantes da Academia Kisaragi tentam encontrar o caminho de volta e procuram sobreviver às forças obscuras que assombram a escola.

Também com classificação restrita e banido da China, Corpse Party pode até ser considerado redundante nessa lista, afinal uma série de terror e mistério é para ser violenta e sanguinária mesmo.
    </p>
        

    <p class="card-text">
        3- Sword Art Online II

        <b>Motivo:</b> Uso de armas de fogo e materiais ofensivos


    <b>Sinopse:</b>

Algum tempo depois do incidente de SAO, Kirito recebe um pedido de ajuda de Seijiro Kikuoka, um membro do Ministério do Japão de Assuntos Internos e Comunicações (Divisão VR). O assunto era uma investigação sobre o incidente “Death Gun”, que ocorreu em um VRMMO famoso chamado Gun Gale Online (GGO). Os jogadores que sofrem um tiro por esse avatar misterioso perdem a vida, mesmo no mundo real.

Convencido de que isso não seria possível, nosso protagonista aceita o pedido de Kikuouka. Kirito vagueia em um mundo desconhecido com único propósito. Obter alguma pista sobre o tal assassino. Contudo, uma franco-atiradora chamada Sinon, que possui um gigantesco rifle chamado “Hecate II”, estende ao namorado de Asuna uma mão amiga. Com a ajuda de Sinon, Kirito decide entrar no “Bullet of Bullets” (BoB), um grande torneio para escolher o jogador mais poderoso dentro do reino de GGO, na esperança de se tornar o alvo do “Death Gun” e fazer contato direto com esse misterioso avatar.

É até complicado imaginar porque logo SAO entrou na lista de animes censurados, pois é um dos mais leves comparados aos outros dessa classificação, mas ele não só sofreu censura na China, como foi banido de lá.

    </p>
    <p class="card-text">
    4 - Kiseijuu: Sei no Kakuritsu (Parasyte: The Maxim)
    <b>Motivo:</b> Gore e violência gráfica

<b>Sinopse:</b>

Izumi Shinichi é um jovem de 17 anos que mora com os pais em um pacato bairro de Tóquio. Certa noite, criaturas parasitas invadem a terra, tomando os cérebros dos humanos, entrando em seus corpos pelos ouvidos ou nariz e, assim, controlando totalmente suas ações e pensamentos. Eis que um desses parasitas tenta entrar no ouvido de Shinichi enquanto ele dorme, mas falha, pois o jovem estava usando fones de ouvido. Então ele resolve adentrar pelo braço direito e fica armazenado lá, logo Shinichi o chama de Migi, dessa forma ambos são obrigados a coexistirem e acabarem com toda a carnificina provocada pelas criaturas.

Por conta de sua enorme carga de violência e sangue (até mais que SNK), Parasyte foi proibido na China por ser consideravelmente sangrento, violento e conter assassinatos graficamente cruéis.
    </p>

    <p class="card-text">
    5 - Death Note
    <b>Motivo:</b> Induz jovens a criarem seu próprio Death Note

<b>Sinopse:</b>

Um shinigami, como um deus da morte, pode matar qualquer um, desde que conheça o rosto de sua vítima e escreva seu nome em um caderno chamado 'Death Note'. Um dia, Ryuk, entediado com o estilo de vida de um shinigami e interessado em ver como um humano usaria o caderno, o joga no reino humano. O prodígio do ensino médio Light Yagami é aquele que tropeça no caderno e testa suas habilidades escrevendo o nome de um criminoso nele. Quando o criminoso morre imediatamente após seu experimento, Light fica chocado e rapidamente reconhece o quão devastador pode ser o poder que ele ganhou agora.

Banido no território da China, o governo acreditou que Death Note era muito perigoso para seus jovens por influencia-los a criarem seus próprios cadernos de nomes com vítimas, assim fazendo com que as autoridades temessem que essas crianças se tornassem assassinos em seu futuro.
    </p>

    <p class="card-text">
    6 - Psycho-Pass
    <b>Motivo:</b> Incentiva a rebelião, o crime e glorifica a violência

<b>Sinopse:</b>

“Em um mundo onde o princípio de que “todos são inocentes até que se prove o contrário” não se aplica, o julgamento é dado por um sistema que analisa o estado mental das pessoas e as categoriza como potenciais criminosos ou não, intitulado de “Sybil”. É dada também uma probabilidade de “cura” (terapia), de recuperação mental desses indivíduos.

Caso essas pessoas sejam classificadas como irrecuperáveis, sua sentença pode ser a prisão ou a morte, mesmo que não tenham cometido crime nenhum. Como auxílio em suas investigações, a polícia usa um grupo de prisioneiros “especiais”, chamados Executores (Enforcers), para ajudá-los a capturar ou matar esses indivíduos “não mais necessários”. Para tal, utilizam uma arma especial, chamado de Dominators, ou “Os olhos da Sibila”.”

O anime de suspense psicológico foi proibido no território chinês por ter um enredo que, segundo eles, glorifica a violência e incentiva não apenas a rebelião, quanto também a delinquência juvenil.

Enfim, por hoje é só. Deixem aqui nos comentários outros animes que conhecem entre os banidos da China e também não esqueçam de colocar suas opiniões sobre os títulos dessa matéria e se acham “justificável” o motivo esclarecido pelo governo chinês, além de compartilhar em suas redes sociais e com os amigos para termos outras listas semelhantes futuramente.
    </p>
    

